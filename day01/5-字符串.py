# 字符串的定义方式

# 单
name = '你好!'
# 双
a = "素色"
# 三
b = """哈哈哈"""
print(name + a + b)

# 字符串占位符%s

c = "小李"
print("你好我是%s" % c)

# 整数占位符%d
print("你好我我是小李,我今年%d" % 18)

# 浮点数占位符
print("hhhh%f" % 1111)

# 精度控制
"""
m.n来进行控制,m代表小数点前的位数,n代表的是小数点后的位数,支持四舍五入
    %5d   ->  11 -> 空格空格空格11
    %5.2d   ->  11.345 ->空格空格空格11.36
    %.2f   ->  10.567  ->10.57 
"""

num1 = 11
num2 = 1100.345
print("%5d" % num1)
print("%3.2f" % num2)

# {}占位符
number = 1010
print(f"你好呀{number}")
